                poCabeceraControl.UsuarioIngresoLog = lsUsuario[0];
                poCabeceraControl.TerminalIngresoLog = Request.UserHostAddress;
                
                object[] resultado = null;
                ClsDParametroDefecto = new ClsDParametroDefecto();
                if (poCabeceraControl.IdParametroDefecto == 0)
                {
                    resultado = ClsDParametroDefecto.GuardarCabeceraControl(poCabeceraControl);
                }
                else
                {
                    resultado = ClsDParametroDefecto.ActualizarCabecera(poCabeceraControl);
