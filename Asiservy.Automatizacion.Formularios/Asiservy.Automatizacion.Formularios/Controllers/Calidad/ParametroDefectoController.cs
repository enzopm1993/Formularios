                ViewBag.Formulario = new SelectList(ClsDClasificador.ConsultarClasificador(clsAtributos.CodGrupoFormularios), "Codigo", "Descripcion");
                ViewBag.Producto = new SelectList(ClsDClasificador.ConsultarClasificador(clsAtributos.CodGrupoTipoProducto), "Codigo", "Descripcion");
                ViewBag.Defectos = new SelectList(ClsDParametroDefecto.ConsultarDefectos().Where(x => x.EstadoRegistro == clsAtributos.EstadoRegistroActivo).ToList(), "IdDefecto", "Nombre");
             
